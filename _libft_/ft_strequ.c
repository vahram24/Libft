int	ft_strequ(char const *s1, char const *s2)
{
	int		i;
	int		j;

	if (!s1 || !s2)
		return (0);
	i = 0;
	j = 0;
	while (s1[i] && s2[j])
	{
		if (s1[i] != s2[j])
			return (0);
		i++;
		j++;
	}
	if (s1[i] != s2[j])
		return (0);
	return (1);
}
